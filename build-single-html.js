#!/usr/bin/env node
/**
 * iPhone向け単一HTMLビルド
 * Filesアプリ等では同フォルダの app.js / style.css を読めないため、
 * CSS・JSを1ファイルに埋め込んだ Brain_Reflexo.html を生成する。
 */
const fs = require('fs');
const path = require('path');

const root = __dirname;
const indexHtml = fs.readFileSync(path.join(root, 'index.html'), 'utf8');
const css = fs.readFileSync(path.join(root, 'style.css'), 'utf8');
const js = fs.readFileSync(path.join(root, 'app.js'), 'utf8');

// </script> がインラインJS内にあるとHTMLが途切れるためエスケープ
const safeJs = js.replace(/<\/script/gi, '<\\/script');

let html = indexHtml;

// 外部CSSをインラインへ
html = html.replace(
    /<link rel="stylesheet" href="style\.css[^"]*">/,
    `<style>\n${css}\n</style>`
);

// 外部JSをインラインへ（単一ファイル版では app.js 未読込案内は不要）
html = html.replace(
    /<script src="app\.js[^"]*"><\/script>[\s\S]*?<\/html>\s*$/,
    `<script>\n${safeJs}\n</script>\n</body>\n</html>\n`
);

// タイトルに単一ファイル版であることを軽く示す（動作には影響しない）
html = html.replace(
    /<title>([^<]*)<\/title>/,
    '<title>$1 (iPhone / Offline)</title>'
);

// Google Fontsはオフラインでも落ちないよう、失敗時はシステムフォントへ（既存フォールバックに任せる）
// 単一ファイルであることを示すコメントを head 冒頭付近に追加
html = html.replace(
    '<head>',
    `<head>\n    <!-- Self-contained build for iPhone Files app. Generated by build-single-html.js -->`
);

const outName = 'Brain_Reflexo.html';
const outPath = path.join(root, outName);
fs.writeFileSync(outPath, html, 'utf8');

const sizeKb = (Buffer.byteLength(html, 'utf8') / 1024).toFixed(1);
console.log(`Wrote ${outName} (${sizeKb} KB)`);
